## Given a invertible matrix, the following two functions
## will calculate the inverse matrix
## or retrieve the inverse matrix from the cache.

## The function makeCacheMatrix creates a matrix 
## that can store its inverse (calculated using the solve function)

makeCacheMatrix <- function(x = matrix()) {
  m <- NULL
  set <- function(y){
    x <<- y
    m <<- NULL
  }
  get <- function() x
  setinverse <- function(solve) m <<- solve
  getinverse <- function() m
  list(set = set, get = get,
       setinverse = setinverse,
       getinverse = getinverse)

}


## This cacheSolve function produces the inverse of the matrix
## unless the inverse matrix is already cached in which case it retrieves the cached data

cacheSolve <- function(x, ...) {
        ## Return a matrix that is the inverse of 'x'
  m <- x$getinverse()
  if(!is.null(m)) {
    message("getting cached data")
    return(m)
  }
  data <- x$get()
  m <- solve(data, ...)
  x$setinverse(m)
  m
}